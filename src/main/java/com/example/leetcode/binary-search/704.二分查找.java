// @lc code=start
class Solution {
    public int search(int[] nums, int target) {
        // length：左闭右开
        // 所以while条件是<时候即是空集，且在左右赋值时候也需要保持一个左闭右开
        int left = 0;
        int right = nums.length;

        while (left < right) {
            int mid = left + (right - left) / 2;
            if (nums[mid] == target) {
                return mid;
            } else if (nums[mid] > target) {
                right = mid;
            } else if (nums[mid] < target) {
                left = mid + 1;
            }
        }
        return -1;
    }
}
